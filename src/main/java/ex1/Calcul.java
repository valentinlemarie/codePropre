package ex1;

public class Calcul {

	/*
	 * additionne 2 variables
	 * @param a,b
	 * 			 les variables additionnes
	 * @return le resultat de l'addtion
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/*
	 * soustrait 2 variables
	 * @param a,b
	 * 			 les variables soustrait
	 * @return le resultat de la soustraction
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
